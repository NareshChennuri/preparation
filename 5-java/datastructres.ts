/*

Array: A collection of elements, usually of the same type, stored in contiguous memory locations. Accessing elements by index is fast, but insertion and deletion can be less efficient.

Linked List: A collection of nodes, where each node contains data and a reference to the next node in the sequence. Linked lists can be singly linked (next reference only) or doubly linked (next and previous references).

Stack: A linear data structure that follows the Last-In-First-Out (LIFO) principle. Elements are added and removed from the same end (top).

Queue: A linear data structure that follows the First-In-First-Out (FIFO) principle. Elements are added to the back (enqueue) and removed from the front (dequeue).

Deque (Double-ended Queue): A linear data structure that allows insertion and deletion at both ends.

Tree: A hierarchical data structure with a root node and child nodes. Common types include binary trees, binary search trees, and AVL trees.

Graph: A collection of nodes (vertices) connected by edges. Graphs can be directed or undirected, and they can have cycles (cyclic) or not (acyclic).

Heap: A specialized tree-based data structure that satisfies the heap property. Common types include binary heaps and priority queues.

Hash Table: A data structure that stores key-value pairs, allowing fast insertion, deletion, and retrieval based on the keys.

Hash Map: A specific type of hash table that implements a map interface, commonly used for dictionary-like functionality.

Set: A collection that stores unique elements with no particular order.

Map: A collection that stores key-value pairs, where keys are unique and each key maps to exactly one value.

Trie: A tree-like data structure used to store a dynamic set of strings, typically used for efficient string search operations.

Bloom Filter: A space-efficient probabilistic data structure used to test whether an element is a member of a set. It may produce false positives but no false negatives.

Disjoint-Set (Union-Find): A data structure that tracks a set of elements partitioned into disjoint sets. It's often used to manage dynamic connectivity between elements.

Segment Tree: A tree-like data structure used for various range query operations, such as finding minimum, maximum, or sum in a given range.

Suffix Array: A sorted array of all the suffixes of a given string. It's used for efficient string pattern matching.

Skip List: A data structure that allows fast search within an ordered sequence of elements, similar to a balanced binary search tree.

*/