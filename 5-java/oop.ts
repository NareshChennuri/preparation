/*

Java is a popular object-oriented programming language, and it is built around several core principles of 

object-oriented programming (OOP) principles help in designing and structuring code in such a way that 
promotes modularity, reusability, and maintainability. 

core features are;
---------------------------
Class and Object: 

Classes are blueprints for creating objects, which are instances of those classes. 
Classes define the attributes (fields) and behaviors (methods) that objects of that class will possess.

Encapsulation: 

Encapsulation is the concept of bundling data (fields) and methods that operate on the data into a single unit, i.e., the class. 
It also involves controlling the access to the data through access modifiers like private, protected, and public. 
Encapsulation helps in hiding the internal details of an object's implementation and providing controlled access to its functionality.

Inheritance: 
Inheritance allows a class (subclass or derived class) to inherit attributes and behaviors from another class (superclass or base class). 
This promotes code reuse and the creation of a hierarchical structure of classes. 
The extends keyword is used to establish inheritance relationships in Java.

Polymorphism: 
can be achieved through method overriding and method overloading. 

Method overriding concept of inheritance, which involves providing a specific implementation of a method in a subclass, while 
method overloading is providing multiple methods with the same name but different parameter lists.

Abstraction: is important for properties and methods of an object while hiding the unnecessary details. 
for Abstraction we use Abstract classes and interfaces.

Dependency Injection: This principle involves providing the required dependencies (objects or services) to a class from external sources, rather than the class creating them itself. It helps in achieving loose coupling between classes and makes the code more modular and testable.



Association, Aggregation, and Composition: These are ways to establish relationships between classes. Association represents a relationship between two classes, aggregation represents a "whole-part" relationship (where one class is composed of one or more instances of another class), and composition represents a stronger form of aggregation, implying ownership and lifecyle management of the contained objects.

Interfaces: An interface defines a contract that classes must adhere to. It specifies a set of methods that implementing classes must provide. Interfaces allow multiple inheritance of behavior and are a way to achieve abstraction.


Single Responsibility Principle (SRP): This principle suggests that a class should have only one reason to change, meaning it should have only one responsibility. This promotes maintainability by reducing the impact of changes.

Open/Closed Principle (OCP): The OCP states that classes should be open for extension but closed for modification. This means that you should be able to add new functionality to a class without altering its existing code.

Liskov Substitution Principle (LSP): This principle states that objects of a superclass should be replaceable with objects of a subclass without affecting the correctness of the program. In other words, subclasses should be usable in place of their parent classes without causing unexpected behavior.

Interface Segregation Principle (ISP): The ISP suggests that clients should not be forced to depend on interfaces they do not use. In other words, interfaces should be fine-grained and specific to the needs of the classes that implement them.

These principles collectively provide a solid foundation for designing object-oriented systems in Java that are modular, maintainable, and adaptable.

*/